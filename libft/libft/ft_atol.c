long	ft_atol(const char *str)
{
	int		i;
	int		sign;
	long	res;

	i = 0;
	while (str[i] == ' ' || (str[i] >= 0 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * sign);
}
